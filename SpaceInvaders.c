int gameResult(){
	int aiScore = 0, humanScore = 0, i, j, k;
	for(i=6;i>=1;--i){
		for(j=1;j<=7;++j){
			if(game[i][j]==0) continue;
			
			//Checking cells to the right
			if(j<=4){
				for(k=0;k<4;++k){ 
					if(game[i][j+k]==1) aiScore++;
					else if(game[i][j+k]==2) humanScore++;
					else break; 
				}
				if(aiScore==4)return 1; else if (humanScore==4)return 2;
				aiScore = 0; humanScore = 0;
			} 
			
			//Checking cells up
			if(i>=4){
				for(k=0;k<4;++k){
					if(game[i-k][j]==1) aiScore++;
					else if(game[i-k][j]==2) humanScore++;
					else break;
				}
				if(aiScore==4)return 1; else if (humanScore==4)return 2;
				aiScore = 0; humanScore = 0;
			} 
			
			//Checking diagonal up-right
			if(j<=4 && i>= 4){
				for(k=0;k<4;++k){
					if(game[i-k][j+k]==1) aiScore++;
					else if(game[i-k][j+k]==2) humanScore++;
					else break;
				}
				if(aiScore==4)return 1; else if (humanScore==4)return 2;
				aiScore = 0; humanScore = 0;
			}
			
			//Checking diagonal up-left
			if(j>=4 && i>=4){
				for(k=0;k<4;++k){
					if(game[i-k][j-k]==1) aiScore++;
					else if(game[i-k][j-k]==2) humanScore++;
					else break;
				} 
				if(aiScore==4)return 1; else if (humanScore==4)return 2;
				aiScore = 0; humanScore = 0;
			}  
		}
	}
	
	for(j=1;j<=7;++j){
			//Game has not ended yet
			if(game[1][j]==0)return -1;
	}
	//Game draw!
	return 0;
}
